public class Vertex4 {
  public float x0 = 0;
  public float y0 = 0;
  public float x1 = 0;
  public float y1 = 0;
  public float x2 = 0;
  public float y2 = 0;
  public float x3 = 0;
  public float y3 = 0;

  public Vertex4(float x0, float y0, float x1, float y1, float x2, float y2,
      float x3, float y3) {
    this.x0 = x0;
    this.y0 = y0;
    this.x1 = x1;
    this.y1 = y1;
    this.x2 = x2;
    this.y2 = y2;
    this.x3 = x3;
    this.y3 = y3;
  }

  public Vertex4() {

  }
}
